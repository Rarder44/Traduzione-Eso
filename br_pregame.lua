local ESNGetString = GetString
function GetString(...)
        if ZO_GammaAdjust ~= nil then
                ZO_GammaAdjustMainText:SetFont("Cervanteso/fonts/univers67.otf|22");
                ZO_GammaAdjustSubText:SetFont("Cervanteso/fonts/univers67.otf|22");
        end

        if ZO_CharacterSelect ~= nil then
                local demWidth, demHeight = ZO_CharacterSelectCreate:GetDimensions()
                ZO_CharacterSelectCreate:SetDimensions(190, demHeight);

                ZO_CharacterSelectSelectedName:SetFont("Cervanteso/fonts/univers67.otf|28|thick-outline");
                ZO_CharacterSelectSelectedRace:SetFont("Cervanteso/fonts/univers67.otf|24|thick-outline");
                ZO_CharacterSelectSelectedClassLevel:SetFont("Cervanteso/fonts/univers67.otf|24|thick-outline");
                ZO_CharacterSelectSelectedLocation:SetFont("Cervanteso/fonts/univers67.otf|18|thick-outline");
        end

        if CCEditFont ~= nil then
                CCEditFont = CreateFont("CCEditFont")
                CCEditFont:SetFont("Cervanteso/fonts/univers67.otf|20")
        end

        if (ESNIsFirstFonts <= 2) then
                ESNFont()
        end
        return ESNGetString(...)
end

